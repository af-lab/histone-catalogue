    ##residue and so we need to "unfix" the annotation
    if (substr ($protein, 1) ne $reference{$symbol}) {
      say "Found different sequences for $symbol";
      $mistakes++;
    }
  }
  say "Total number of mistakes found is: $mistakes";
}

